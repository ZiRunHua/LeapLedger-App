class TextWidget extends StatefulWidget {
  const TextWidget({super.key});

  @override
  State<TextWidget> createState() => _TextWidgetState();
}

class _TextWidgetState extends State<TextWidget> {
  late final ExpenseChartCubit _cubit;
  @override
  void initState() {
    _cubit = ExpenseChartCubit(account: UserBloc.currentAccount)..loadCategoryRank();
    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: Container(
          child: BlocProvider<ExpenseChartCubit>.value(
        value: _cubit,
        child: BlocBuilder<ExpenseChartCubit, ExpenseChartState>(
          builder: (context, state) {
            if (state is ExpenseCategoryRankLoaded) {
              return CategoryPieChart(_cubit.categoryList);
            }
            return SizedBox();
          },
        ),
      )),
    );
  }
}
